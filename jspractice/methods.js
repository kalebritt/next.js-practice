//Math.random() is a method that generates a random number between 0 and 1
console.log(Math.random() * 100);
//Math.floor() is a method that rounds a number to the nearest integer
console.log(Math.floor(Math.random() * 100));
//Math.ceil() is a method that rounds a number to the nearest integer between 0 and 1
console.log(Math.ceil(43.8));
//Number.isInteger() is a method that checks if a number is an integer between 0 and 1
console.log(Number.isInteger(2017));
